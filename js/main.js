//创建地图
var box = $('#box li')
function createMap() {
    box.each(function (index) {
        box.eq(index).removeClass(); //清除上关数据
    });
    box.each(function (index) {
        if(map[level][index]){
            box.eq(index).addClass('type' + map[level][index]);
        }
    })
}
createMap();

//生成蛇
function createSnake() {
    box.each(function (index) {
        box.eq(index).removeClass('type3');
    });

    box.each(function (index) {
        box.eq(snakeLevel[index]).addClass('type3')
    })
}
createSnake();

//创建食物
function createFood() {
    foodPosition = Math.round(Math.random()*2500);
    if (map[level][foodPosition] == 1) {
        createFood();
    }else if (box.eq(foodPosition).hasClass('type3')) {
        createFood();
    }else{
        box.eq(foodPosition).addClass('type2');
    }
}
createFood();

//蛇移动
function move(step) {
    // debugger
    var snakeTo = [];
    var newsnakehead = snakeLevel[0] + step;
    for (var i = 0; i < snakeLevel.length; i++) {
        if (i == 0) {
            snakeTo[i] = newsnakehead;
        }else{
            snakeTo[i] = snakeLevel[i-1];
        }
    }
    snakeLevel = snakeTo;
    if(newsnakehead == foodPosition){
        eat()
    }else if (box.eq(newsnakehead).hasClass('type1')) {
        alert('game over');
        clearInterval(move_top);
        clearInterval(move_left);
        clearInterval(move_right);
        clearInterval(move_bottom);

        snakeLevel = snake[level];
        createMap();
        createFood();
        level = 0;
        direction = 0;
    } else if (box.eq(newsnakehead).hasClass('type3')) {
        alert('game over');
        clearInterval(move_top);
        clearInterval(move_left);
        clearInterval(move_right);
        clearInterval(move_bottom);

        snakeLevel = snake[level];
        createMap();
        createFood();
        level = 0;
        direction = 0
    }
    createSnake();
}

//吃食物
function eat() {
    box.eq(foodPosition).removeClass('type2');
    snakeLevel.length++;
    createFood();
    num++;
    setnum()
    if (num == passnum[level]) {
        if (level == 4) {
            alert('玩的一手好蛇');
        }
        alert('恭喜过关');
        clearInterval(move_top);
        clearInterval(move_left);
        clearInterval(move_right);
        clearInterval(move_bottom);

        level++;
        snakeLevel = snake[level];
        createMap();
        createFood();
        num = 3;
        setnum();
    }

}

